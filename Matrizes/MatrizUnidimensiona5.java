public class MatrizUnidimensiona5 {
    public static void main(String[] args) {
        
        int [] vetor = new int [5];
        vetor[0] = 50;
        vetor[1] = 26;
        vetor[2] = 51;
        vetor[3] = 18;
        vetor[4] = 39;

        for (int i = 0;i<vetor.length;i++) {
            System.out.println(vetor[i]);
        }
    }
}
